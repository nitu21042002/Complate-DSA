# Write a program to convert strings to integers and integers to strings.

# String to Integer
str_num = "100"
int_num = int(str_num)

print("String:", str_num, type(str_num))
print("Integer:", int_num, type(int_num))

print()

# Integer to String
num = 200
str_value = str(num)

print("Integer:", num, type(num))
print("String:", str_value, type(str_value))